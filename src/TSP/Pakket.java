package TSP;

public class Pakket {
    int number;
    int x;
    int y;

    public void setNumber(int number) {
        this.number = number;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getNumber() {
        return number;
    }
}
